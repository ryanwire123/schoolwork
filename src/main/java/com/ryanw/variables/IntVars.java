package com.ryanw.variables;

public class IntVars {
    public static void main(String[] args) {
        int n = 25;
        int m = 25;
        System.out.println(n);
        System.out.println(m);
        n = n / 5;
        m += 5;
        System.out.println(n);
        System.out.println(m);


        n = 25;
        n /= 3;
        System.out.println(n);
    }
}
